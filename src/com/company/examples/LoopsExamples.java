package com.company.examples;

public class LoopsExamples {
    /**
     * an example of a for loop,
     * signature whatever parameters are and whatever it returns
     */
    public void forLoopExample() {
        for(int i = 0; i < 8; i ++) {
            System.out.printf("i is: %d %n", i);
        }

    }
}
